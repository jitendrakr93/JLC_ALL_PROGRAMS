package com.classcore.palle01;

public class Lab10 {

	public static void main(String[] args) {
		int i=10;
		int j=10;
		int k=10;
		int l=10;
		System.out.println(++i);
		System.out.println(i);
		System.out.println(j++);
		System.out.println(j);
		System.out.println(--k);
		System.out.println(k);
		System.out.println(l--);
		System.out.println(l);

	}

}
